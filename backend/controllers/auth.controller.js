import { validationResult } from 'express-validator';
import jwt from 'jsonwebtoken';
import crypto from 'crypto';
import User from '../models/User.model.js';
import { Wallet } from '../models/Wallet.model.js';
import notificationHelper from '../utils/notificationHelper.js';
import { applyReferralCode } from './referral.controller.js';

// Generate JWT Token
const generateToken = (userId) => {
  return jwt.sign({ userId }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRE || '7d'
  });
};

// Generate Refresh Token
const generateRefreshToken = (userId) => {
  return jwt.sign({ userId }, process.env.JWT_REFRESH_SECRET, {
    expiresIn: process.env.JWT_REFRESH_EXPIRE || '30d'
  });
};

// @desc    Register new user
// @route   POST /api/auth/register
// @access  Public
export const register = async (req, res) => {
  try {
    // Validate request
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { fullName, email, password, role, phone, city, area, referralCode } = req.body;

    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'User with this email already exists'
      });
    }

    // Generate unique referral code for new user
    let newUserReferralCode;
    let isUnique = false;
    
    while (!isUnique) {
      newUserReferralCode = crypto.randomBytes(4).toString('hex').toUpperCase();
      const existingCode = await User.findOne({ referralCode: newUserReferralCode });
      if (!existingCode) {
        isUnique = true;
      }
    }

    // Create user
    const user = await User.create({
      fullName,
      email,
      password,
      role,
      phone,
      city,
      area,
      referralCode: newUserReferralCode
    });

    // Apply referral code if provided
    let referralResult = null;
    if (referralCode && referralCode.trim()) {
      referralResult = await applyReferralCode(user._id, referralCode.trim(), user.role);
    }

    // Create wallet for user
    await Wallet.create({
      user: user._id,
      balance: 0
    });

    // Generate tokens
    const token = generateToken(user._id);
    const refreshToken = generateRefreshToken(user._id);

    res.status(201).json({
      success: true,
      message: 'User registered successfully',
      data: {
        user: {
          id: user._id,
          fullName: user.fullName,
          email: user.email,
          role: user.role,
          phone: user.phone,
          city: user.city,
          area: user.area,
          profileCompleted: user.profileCompleted,
          referralCode: user.referralCode
        },
        token,
        refreshToken,
        referral: referralResult
      }
    });
  } catch (error) {
    console.error('Register error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error during registration',
      error: error.message
    });
  }
};

// @desc    Login user
// @route   POST /api/auth/login
// @access  Public
export const login = async (req, res) => {
  try {
    // Validate request
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation failed',
        errors: errors.array()
      });
    }

    const { email, password } = req.body;

    // Find user and include password
    const user = await User.findOne({ email }).select('+password');
    
    if (!user) {
      return res.status(401).json({
        success: false,
        message: 'Invalid email or password'
      });
    }

    // Check password
    const isPasswordValid = await user.comparePassword(password);
    
    if (!isPasswordValid) {
      return res.status(401).json({
        success: false,
        message: 'Invalid email or password'
      });
    }

    // Check if account is active
    if (!user.isActive) {
      return res.status(401).json({
        success: false,
        message: 'Account is deactivated'
      });
    }

    // Update last login
    user.lastLogin = new Date();
    await user.save();

    // Send profile incomplete notification if profile not completed
    if (!user.profileCompleted) {
      try {
        await notificationHelper.profileIncomplete(user._id);
      } catch (notifError) {
        console.error('Error sending profile notification:', notifError);
      }
    }

    // Generate tokens
    const token = generateToken(user._id);
    const refreshToken = generateRefreshToken(user._id);

    res.status(200).json({
      success: true,
      message: 'Login successful',
      data: {
        user: {
          id: user._id,
          fullName: user.fullName,
          email: user.email,
          role: user.role,
          phone: user.phone,
          city: user.city,
          area: user.area,
          profilePicture: user.profilePicture,
          profileCompleted: user.profileCompleted,
          rating: user.rating,
          completedJobs: user.completedJobs
        },
        token,
        refreshToken
      }
    });
  } catch (error) {
    console.error('Login error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error during login',
      error: error.message
    });
  }
};

